
// Expressions Vs Statements

// A statement is the smallest element of a language
// that expresses some action to be carried out.
val foo = 5

// An expression is a combination of GTE one values,
// constants, variables and functions that a language
// interprets to *compute a value*.
val bar = if (10 % 2 == 0) "even" else "odd"
//        ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^ 
//        This bit above here is an expression!

