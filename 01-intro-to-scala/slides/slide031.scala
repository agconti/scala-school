
// Data Structures: Maps

// Using tuples, maps are very easy to create.
val numbers = Map(1 -> 2, 3 -> 4)
// numbers: Map[Int,Int] = Map(1 -> 2, 3 -> 4)

// You can think of Maps a set of tuples 
// where uniquness is determined by the key

// Moreover...
//   Map(1 -> "one", 2 -> "two") 
//     expands into 
//   Map((1, "one"), (2, "two")) 

